package com.cleversonfernando.TestesUnitarios;

public class Calcular {

    public int somar(int priprimeiroValor, int segundoValor) {
        return priprimeiroValor + segundoValor;
    }

    public int subtrair(int priprimeiroValor, int segundoValor) {
        return priprimeiroValor - segundoValor;
    }

    public int dividir(int priprimeiroValor, int segundoValor) {
        return priprimeiroValor / segundoValor;
    }
}
